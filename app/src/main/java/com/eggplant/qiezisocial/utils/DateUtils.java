    public static String getWeek(Long data) {
        String Week = "星期";
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");//也可将此值当参数传进来
        Date curDate = new Date(data);
        String pTime = format.format(curDate);
        Calendar c = Calendar.getInstance();
        try {
            c.setTime(format.parse(pTime));
        } catch (ParseException e) {
            e.printStackTrace();
        }

        Date today = new Date(System.currentTimeMillis());
        String tTime = format.format(today);
        Calendar t = Calendar.getInstance();
        try {
            t.setTime(format.parse(tTime));
        } catch (ParseException e) {
            e.printStackTrace();
        }
        int tWeek = t.get(Calendar.DAY_OF_WEEK);
        int cWeek = c.get(Calendar.DAY_OF_WEEK);
        if (tWeek - cWeek == 1) {
            return "昨天";
        }
        switch (cWeek) {
            case 1:
                Week += "天";

                break;
            case 2:
                Week += "一";
                break;
            case 3:
                Week += "二";
                break;
            case 4:
                Week += "三";
                break;
            case 5:
                Week += "四";
                break;
            case 6:
                Week += "五";
                break;
            case 7:
                Week += "六";
                break;
            default:
                break;
        }
        return Week;
    }

